
let a = 10;
let b = 3;

// 1. Addition
let sum = a + b; // 10 + 3 = 13
console.log("Addition:", sum);

// 2. Subtraction
let difference = a - b; // 10 - 3 = 7
console.log("Subtraction:", difference);

// 3. Multiplication
let product = a * b; // 10 * 3 = 30
console.log("Multiplication:", product);

// 4. Division
let quotient = a / b; // 10 / 3 = 3.3333
console.log("Division:", quotient);

// 5. Modulus (Remainder)
let remainder = a % b; // 10 % 3 = 1
console.log("Modulus:", remainder);

// 6. Exponentiation
let power = a ** b; // 10^3 = 1000
console.log("Exponentiation:", power);
